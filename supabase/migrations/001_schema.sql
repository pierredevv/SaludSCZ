CREATE TYPE public.enfermedad_tipo AS ENUM ('viral', 'bacteriana', 'parasitaria');
CREATE TYPE public.usuario_rol AS ENUM ('admin', 'medico', 'enfermero', 'ciudadano');
CREATE TYPE public.paciente_sexo AS ENUM ('M', 'F', 'O');
CREATE TYPE public.caso_estado AS ENUM ('sospechoso', 'confirmado', 'descartado', 'recuperado', 'fallecido');
CREATE TYPE public.caso_confirmacion AS ENUM ('clinica', 'laboratorio', 'epidemiologica');
CREATE TYPE public.sintoma_gravedad AS ENUM ('leve', 'moderado', 'grave');
CREATE TYPE public.caso_sintoma_intensidad AS ENUM ('leve', 'moderada', 'intensa');
CREATE TYPE public.alerta_nivel AS ENUM ('verde', 'amarillo', 'naranja', 'rojo');
CREATE TYPE public.centro_tipo AS ENUM ('hospital', 'clinica', 'centro_de_salud', 'puesto_de_salud');

-- ============================================
-- 1. ZONA (sin dependencias externas)
-- ============================================

CREATE TABLE public.zona (
    id_zona     BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nombre      VARCHAR(100) NOT NULL,
    distrito    VARCHAR(50),
    municipio   VARCHAR(100) NOT NULL DEFAULT 'Santa Cruz de la Sierra',
    latitud     DECIMAL(9, 6),
    longitud    DECIMAL(9, 6),
    poblacion   INTEGER,  -- Agregado: necesario para tasa de incidencia por 100k hab
    created_at  TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at  TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ============================================
-- 2. ENFERMEDAD
-- ============================================

CREATE TABLE public.enfermedad (
    id_enfermedad BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nombre        VARCHAR(100) NOT NULL UNIQUE,
    tipo          public.enfermedad_tipo NOT NULL,
    descripcion   TEXT,
    vector        VARCHAR(100),  -- ej: 'Aedes aegypti'
    created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at    TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ============================================
-- 3. CENTRO_SALUD (depende de zona)
-- ============================================

CREATE TABLE public.centro_salud (
    id_centro  BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nombre     VARCHAR(150) NOT NULL,
    tipo       public.centro_tipo,
    direccion  VARCHAR(200),
    telefono   VARCHAR(20),
    id_zona    BIGINT NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_centro_salud_zona FOREIGN KEY (id_zona)
        REFERENCES public.zona(id_zona) ON DELETE RESTRICT
);

-- ============================================
-- 4. USUARIO (depende de centro_salud)
-- ============================================

CREATE TABLE public.usuario (
    id_usuario      BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nombre          VARCHAR(150) NOT NULL,
    email           VARCHAR(100) NOT NULL UNIQUE,
    contrasena_hash VARCHAR(255) NOT NULL,
    rol             public.usuario_rol NOT NULL,
    id_centro       BIGINT,
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_usuario_centro FOREIGN KEY (id_centro)
        REFERENCES public.centro_salud(id_centro) ON DELETE SET NULL
);

-- ============================================
-- 5. PACIENTE (depende de zona)
-- ============================================

CREATE TABLE public.paciente (
    id_paciente      BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nombre           VARCHAR(100) NOT NULL,
    apellido         VARCHAR(100) NOT NULL,
    fecha_nacimiento DATE,
    sexo             public.paciente_sexo,
    telefono         VARCHAR(20),
    direccion        VARCHAR(200),
    id_zona          BIGINT,
    created_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at       TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_paciente_zona FOREIGN KEY (id_zona)
        REFERENCES public.zona(id_zona) ON DELETE SET NULL
);

-- ============================================
-- 6. CASO (tabla central — une paciente, enfermedad, centro, usuario)
-- ============================================

CREATE TABLE public.caso (
    id_caso               BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    fecha_registro        DATE NOT NULL DEFAULT CURRENT_DATE,
    fecha_inicio_sintomas DATE,
    estado                public.caso_estado NOT NULL DEFAULT 'sospechoso',
    confirmacion          public.caso_confirmacion DEFAULT 'clinica',
    id_paciente           BIGINT NOT NULL,
    id_enfermedad         BIGINT NOT NULL,
    id_centro             BIGINT NOT NULL,
    id_usuario            BIGINT NOT NULL,
    created_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at            TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_caso_paciente FOREIGN KEY (id_paciente)
        REFERENCES public.paciente(id_paciente) ON DELETE RESTRICT,
    CONSTRAINT fk_caso_enfermedad FOREIGN KEY (id_enfermedad)
        REFERENCES public.enfermedad(id_enfermedad) ON DELETE RESTRICT,
    CONSTRAINT fk_caso_centro FOREIGN KEY (id_centro)
        REFERENCES public.centro_salud(id_centro) ON DELETE RESTRICT,
    CONSTRAINT fk_caso_usuario FOREIGN KEY (id_usuario)
        REFERENCES public.usuario(id_usuario) ON DELETE RESTRICT
);

-- ============================================
-- 7. SINTOMA
-- ============================================

CREATE TABLE public.sintoma (
    id_sintoma     BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    nombre         VARCHAR(100) NOT NULL UNIQUE,
    descripcion    TEXT,
    nivel_gravedad public.sintoma_gravedad NOT NULL DEFAULT 'leve',
    created_at     TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at     TIMESTAMPTZ NOT NULL DEFAULT now()
);

-- ============================================
-- 8. CASO_SINTOMA (tabla intermedia muchos-a-muchos)
-- ============================================

CREATE TABLE public.caso_sintoma (
    id_caso         BIGINT NOT NULL,
    id_sintoma      BIGINT NOT NULL,
    fecha_aparicion DATE,
    intensidad      public.caso_sintoma_intensidad DEFAULT 'leve',
    created_at      TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (id_caso, id_sintoma),
    CONSTRAINT fk_caso_sintoma_caso FOREIGN KEY (id_caso)
        REFERENCES public.caso(id_caso) ON DELETE CASCADE,
    CONSTRAINT fk_caso_sintoma_sintoma FOREIGN KEY (id_sintoma)
        REFERENCES public.sintoma(id_sintoma) ON DELETE RESTRICT
);

-- ============================================
-- 9. ALERTA (depende de zona)
-- ============================================

CREATE TABLE public.alerta (
    id_alerta     BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    fecha_inicio  DATE NOT NULL DEFAULT CURRENT_DATE,
    fecha_fin     DATE,
    tipo          VARCHAR(100) NOT NULL,  -- 'brote', 'alerta temprana', 'emergencia'
    mensaje       TEXT,
    nivel         public.alerta_nivel NOT NULL DEFAULT 'verde',
    activa        BOOLEAN NOT NULL DEFAULT true,  -- Agregado: para filtrar alertas activas
    id_zona       BIGINT NOT NULL,
    created_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at    TIMESTAMPTZ NOT NULL DEFAULT now(),
    CONSTRAINT fk_alerta_zona FOREIGN KEY (id_zona)
        REFERENCES public.zona(id_zona) ON DELETE RESTRICT
);

