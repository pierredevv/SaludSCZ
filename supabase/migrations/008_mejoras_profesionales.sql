-- 3. VISTA PARA CANAL ENDÉMICO
-- Calcula percentiles históricos por semana epidemiológica
-- ============================================

CREATE OR REPLACE VIEW public.v_canal_endemico AS
WITH casos_semanales AS (
    SELECT
        EXTRACT(WEEK FROM c.fecha_registro)::INT AS semana_epi,
        EXTRACT(YEAR FROM c.fecha_registro)::INT AS anio,
        e.nombre AS enfermedad,
        COUNT(*) AS total
    FROM public.caso c
    JOIN public.enfermedad e ON c.id_enfermedad = e.id_enfermedad
    WHERE c.estado = 'confirmado'
    GROUP BY semana_epi, anio, e.nombre
),
stats AS (
    SELECT
        semana_epi,
        enfermedad,
        PERCENTILE_CONT(0.25) WITHIN GROUP (ORDER BY total) AS p25,
        PERCENTILE_CONT(0.50) WITHIN GROUP (ORDER BY total) AS p50,
        PERCENTILE_CONT(0.75) WITHIN GROUP (ORDER BY total) AS p75,
        AVG(total) AS promedio,
        MAX(total) AS maximo
    FROM casos_semanales
    GROUP BY semana_epi, enfermedad
)
SELECT
    s.semana_epi,
    s.enfermedad,
    ROUND(s.p25::NUMERIC, 1) AS zona_exito,
    ROUND(s.p50::NUMERIC, 1) AS zona_seguridad,
    ROUND(s.p75::NUMERIC, 1) AS zona_alerta,
    ROUND(s.promedio::NUMERIC, 1) AS promedio_historico,
    s.maximo AS maximo_historico,
    COALESCE(actual.total, 0) AS casos_actuales,
    EXTRACT(YEAR FROM CURRENT_DATE)::INT AS anio_actual,
    CASE
        WHEN COALESCE(actual.total, 0) <= s.p25 THEN 'exito'
        WHEN COALESCE(actual.total, 0) <= s.p50 THEN 'seguridad'
        WHEN COALESCE(actual.total, 0) <= s.p75 THEN 'alerta'
        ELSE 'epidemia'
    END AS clasificacion_canal
FROM stats s
LEFT JOIN casos_semanales actual
    ON actual.semana_epi = s.semana_epi
    AND actual.enfermedad = s.enfermedad
    AND actual.anio = EXTRACT(YEAR FROM CURRENT_DATE)::INT
ORDER BY s.enfermedad, s.semana_epi;

-- ============================================
-- 4. VISTA DE BITÁCORA (lectura simplificada)
-- ============================================

CREATE OR REPLACE VIEW public.v_bitacora_reciente AS
SELECT
    id_bitacora,
    tabla_afectada,
    operacion,
    id_registro,
    usuario_email,
    fecha,
    datos_anteriores,
    datos_nuevos
FROM public.bitacora_auditoria
ORDER BY fecha DESC
LIMIT 100;
