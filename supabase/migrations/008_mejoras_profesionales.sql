-- 1. BITÁCORA DE AUDITORÍA MÉDICA
-- ============================================

CREATE TABLE IF NOT EXISTS public.bitacora_auditoria (
    id_bitacora    BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    tabla_afectada VARCHAR(100) NOT NULL,
    operacion      VARCHAR(20) NOT NULL,
    id_registro    BIGINT NOT NULL,
    usuario_email  VARCHAR(150),
    datos_anteriores JSONB,
    datos_nuevos   JSONB,
    fecha          TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE public.bitacora_auditoria ENABLE ROW LEVEL SECURITY;
CREATE POLICY "public_read_bitacora" ON public.bitacora_auditoria FOR SELECT TO public USING (true);
CREATE POLICY "auth_insert_bitacora" ON public.bitacora_auditoria FOR INSERT TO public WITH CHECK (true);

-- Trigger genérico de auditoría para tabla caso
CREATE OR REPLACE FUNCTION public.audit_caso_changes()
RETURNS TRIGGER AS $$
BEGIN
    IF TG_OP = 'INSERT' THEN
        INSERT INTO public.bitacora_auditoria (tabla_afectada, operacion, id_registro, usuario_email, datos_nuevos)
        VALUES ('caso', 'INSERT', NEW.id_caso, current_setting('request.jwt.claims', true)::json->>'email', to_jsonb(NEW));
    ELSIF TG_OP = 'UPDATE' THEN
        INSERT INTO public.bitacora_auditoria (tabla_afectada, operacion, id_registro, usuario_email, datos_anteriores, datos_nuevos)
        VALUES ('caso', 'UPDATE', NEW.id_caso, current_setting('request.jwt.claims', true)::json->>'email', to_jsonb(OLD), to_jsonb(NEW));
    ELSIF TG_OP = 'DELETE' THEN
        INSERT INTO public.bitacora_auditoria (tabla_afectada, operacion, id_registro, usuario_email, datos_anteriores)
        VALUES ('caso', 'DELETE', OLD.id_caso, current_setting('request.jwt.claims', true)::json->>'email', to_jsonb(OLD));
    END IF;
    RETURN COALESCE(NEW, OLD);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trg_audit_caso ON public.caso;
CREATE TRIGGER trg_audit_caso
    AFTER INSERT OR UPDATE OR DELETE ON public.caso
    FOR EACH ROW EXECUTE FUNCTION public.audit_caso_changes();

-- ============================================
-- 2. ALERTAS AUTOMÁTICAS POR UMBRAL EPIDÉMICO
-- ============================================

CREATE OR REPLACE FUNCTION public.evaluar_umbral_alerta()
RETURNS TRIGGER AS $$
DECLARE
    v_id_zona BIGINT;
    v_casos_recientes INT;
    v_poblacion INT;
    v_tasa_ataque NUMERIC;
    v_alerta_existente BOOLEAN;
BEGIN
    -- Obtener zona del paciente del caso insertado
    SELECT p.id_zona INTO v_id_zona
    FROM public.paciente p WHERE p.id_paciente = NEW.id_paciente;

    IF v_id_zona IS NULL THEN RETURN NEW; END IF;

    -- Obtener población
    SELECT poblacion INTO v_poblacion FROM public.zona WHERE id_zona = v_id_zona;
    IF v_poblacion IS NULL OR v_poblacion = 0 THEN RETURN NEW; END IF;

    -- Contar casos confirmados en últimas 2 semanas
    SELECT COUNT(*) INTO v_casos_recientes
    FROM public.caso c
    JOIN public.paciente p ON c.id_paciente = p.id_paciente
    WHERE p.id_zona = v_id_zona
      AND c.estado = 'confirmado'
      AND c.fecha_registro >= (CURRENT_DATE - INTERVAL '14 days');

    v_tasa_ataque := (v_casos_recientes * 100000.0) / v_poblacion;

    -- Verificar si ya hay alerta activa en esa zona
    SELECT EXISTS (
        SELECT 1 FROM public.alerta
        WHERE id_zona = v_id_zona AND activa = true
          AND fecha_inicio >= (CURRENT_DATE - INTERVAL '14 days')
    ) INTO v_alerta_existente;

    IF NOT v_alerta_existente THEN
        IF v_tasa_ataque >= 300.0 THEN
            INSERT INTO public.alerta (fecha_inicio, tipo, mensaje, nivel, activa, id_zona)
            VALUES (CURRENT_DATE, 'brote',
                'ALERTA AUTOMÁTICA: Tasa de ataque crítica (' || ROUND(v_tasa_ataque, 1) || '/100k). ' || v_casos_recientes || ' casos en 14 días.',
                'rojo', true, v_id_zona);
        ELSIF v_tasa_ataque >= 100.0 THEN
            INSERT INTO public.alerta (fecha_inicio, tipo, mensaje, nivel, activa, id_zona)
            VALUES (CURRENT_DATE, 'alerta temprana',
                'ALERTA AUTOMÁTICA: Tasa elevada (' || ROUND(v_tasa_ataque, 1) || '/100k). ' || v_casos_recientes || ' casos en 14 días.',
                'naranja', true, v_id_zona);
        ELSIF v_tasa_ataque >= 30.0 THEN
            INSERT INTO public.alerta (fecha_inicio, tipo, mensaje, nivel, activa, id_zona)
            VALUES (CURRENT_DATE, 'monitoreo',
                'ALERTA AUTOMÁTICA: Incremento moderado (' || ROUND(v_tasa_ataque, 1) || '/100k). ' || v_casos_recientes || ' casos en 14 días.',
                'amarillo', true, v_id_zona);
        END IF;
    END IF;

    RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

DROP TRIGGER IF EXISTS trg_evaluar_alerta ON public.caso;
CREATE TRIGGER trg_evaluar_alerta
    AFTER INSERT ON public.caso
    FOR EACH ROW
    WHEN (NEW.estado = 'confirmado')
    EXECUTE FUNCTION public.evaluar_umbral_alerta();

